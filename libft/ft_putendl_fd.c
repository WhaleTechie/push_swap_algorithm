/*
** Outputs the string ’s’ to the given file descriptor, followed by a newline.
** #1. The string to output.
** #2. The file descriptor on which to write.
*/

#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	if (s)
	{
		ft_putstr_fd(s, fd);
		write(fd, "\n", 1);
	}
}
